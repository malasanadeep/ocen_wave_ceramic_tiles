    function calculate() {
      const length = parseFloat(document.getElementById('length').value);
      const width = parseFloat(document.getElementById('width').value);
      const tileSize = parseFloat(document.getElementById('tileSize').value);
      const area = length * width;
      const areaInFeet = area * 10.7639;
      const requiredTiles = Math.ceil(area / tileSize);
      const requiredBoxes = Math.ceil(requiredTiles / 12); // assuming each box contains 12 tiles

      document.getElementById('squareMeter').value = area.toFixed(2);
      document.getElementById('squareFeet').value = areaInFeet.toFixed(2);
      document.getElementById('requiredTiles').value = requiredTiles;
      document.getElementById('requiredBoxes').value = requiredBoxes;
    }

    function clearResults() {
      document.getElementById('length').value = '';
      document.getElementById('width').value = '';
      document.getElementById('tileSize').value = '0.36';
      document.getElementById('squareMeter').value = '';
      document.getElementById('squareFeet').value = '';
      document.getElementById('requiredTiles').value = '';
      document.getElementById('requiredBoxes').value = '';
    }
